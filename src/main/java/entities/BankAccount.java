package entities;

public class BankAccount {
	
	private String accountOwner;
	private String iban;
	private String bic;
	private String bankName;
	
	public String getAccountOwner() {
		return accountOwner;
	}
	public String getIBAN() {
		return iban;
	}
	public String getBic() {
		return bic;
	}
	public String getBankName() {
		return bankName;
	}
}
